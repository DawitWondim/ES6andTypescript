
// Arrow function with no parameters and one line of code.
const sayName = () => "My name is Dawit"

// Arrow function with one parameter and one line of code.
const add = num1 => num1 + 10

// Arrow function with two parameters and one line of code.
const math = (a,b) => a * b + 25


console.log(sayName());
console.log(add(1));
console.log(math(3,5));